public class Fibonacci {
	/**
	 * 値が50未満のフィボナッチ数列を表示
	 * @param args
	 */
	public static void main(String[] args) {
		int lo = 1;
		int hi = 1;
		System.out.println("Fibonacci sequence");
		System.out.println(lo);
		while (hi < 50) {
			System.out.println(hi);
			hi = lo + hi;	// 新しいhi
			lo = hi - lo;	// 新しいloは（合計 - 古いlo）、すなわち古いhi
		}
	}
}
